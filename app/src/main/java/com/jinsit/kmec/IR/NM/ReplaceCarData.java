package com.jinsit.kmec.IR.NM;

public class ReplaceCarData {

	public String REPL_CAR_IF;
	public String REPL_CAR_IF_NM;

	public String getREPL_CAR_IF() {
		return REPL_CAR_IF;
	}

	public void setREPL_CAR_IF(String rEPL_CAR_IF) {
		REPL_CAR_IF = rEPL_CAR_IF;
	}

	public String getREPL_CAR_IF_NM() {
		return REPL_CAR_IF_NM;
	}

	public void setREPL_CAR_IF_NM(String rEPL_CAR_IF_NM) {
		REPL_CAR_IF_NM = rEPL_CAR_IF_NM;
	}

	public ReplaceCarData(String rEPL_CAR_IF, String rEPL_CAR_IF_NM) {
		super();
		REPL_CAR_IF = rEPL_CAR_IF;
		REPL_CAR_IF_NM = rEPL_CAR_IF_NM;
	}

	public ReplaceCarData() {
		super();
	}

}
